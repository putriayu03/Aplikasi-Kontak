    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        String selected = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        if(selected != "Pilih Kontak")
            id = Integer.valueOf(selected.substring(selected.lastIndexOf("-") + 1));
        else
            id = 0;
        if(id != 0){
            try
            {
                Class.forName("com.mysql.cj.jdbc.Driver");
                sqlConn = DriverManager.getConnection(dataConn, username, password);
                pst = sqlConn.prepareStatement("select nama, no_telp, kategori, email, alamat from kontak WHERE id=?");

                pst.setString(1, String.valueOf(id));

                rs = pst.executeQuery();
                rs.next(); 
                jTextField1.setText(rs.getString("nama"));
                jTextField2.setText(rs.getString("no_telp"));
                jComboBox2.setSelectedItem(rs.getString("kategori"));
                jTextField3.setText(rs.getString("email"));
                jTextArea1.setText(rs.getString("alamat"));
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        else {
            jTextField1.setText("");
            jTextField2.setText("");
            jComboBox2.setSelectedIndex(0);
            jTextField3.setText("");
            jTextArea1.setText("");
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

