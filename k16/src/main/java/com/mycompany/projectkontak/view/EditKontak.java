    private void updateComboBox(){
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn, username, password);
            pst = sqlConn.prepareStatement("select id, nama, no_telp from kontak WHERE user_id = ?");
            pst.setInt(1, ConstUtil.user_id);
            
            for(int x = jComboBox1.getItemCount()-1; x > 0; x--){
                jComboBox1.removeItemAt(x);
            }
            
            rs = pst.executeQuery();
            while(rs.next()){
                jComboBox1.addItem(rs.getString("nama")+" ("+rs.getString("no_telp")+") id-"+rs.getString("id"));
            }
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

